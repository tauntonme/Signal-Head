void	adc_read_updater	(bool	full)	{
	uint16_t *	read_addr = adc_buff;
	if	(full)							//	Ping pong buffer is either half full or full
		read_addr += (ADC_BUFFLEN / 2);	//	if full then read from upper half as is now writing to lower half
	for	(int i = 0; i < NUMOF_ADC_CHANS; i++)	{
		adc_prev_results[i] = adc_new_results[i];
		adc_new_results[i] = 0;
	}
	for	(int i = 0; i < ((ADC_BUFFLEN / 2) / NUMOF_ADC_CHANS); i++)	{
		for	(int j = 0; j < NUMOF_ADC_CHANS; j++)	{
			adc_new_results[j] += *read_addr++;
		}
	}
}

